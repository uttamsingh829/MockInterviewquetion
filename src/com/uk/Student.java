package com.uk;

public class Student {
	private int id;
	private String name;
public void Student(int id,String name) {
	this.id=id;
	this.name=name;
}
public void setId(int id) {
	this.id=id;
}
public int getId() {
	return id;
}
public void Student(String name) {
	this.name=name;
}
public String getString() {
	return name;
}
public void setName(String name) {
	this.name=name;
}
}
